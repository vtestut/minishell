int	ft_isalpha(int c)
{
	return (g_lookup_table[(u_int8_t)c]);
}

int	ft_isalphastr(const char *str)
{
	size_t	i;
	int		r;

	i = ft_strlen(str);
	r = 1;
	while (i--)
		r &= g_lookup_table[(u_int8_t)str[i]];
	return (r);
}
